num_1st = int(input("Give me the first number: "))
num_2nd = int(input("Give me the second number: "))

print("Thank you!")
print(f"{num_1st} + {num_2nd} = {int(num_1st+num_2nd)}")
print(f"{num_1st} - {num_2nd} = {int(num_1st-num_2nd)}")
print(f"{num_1st} / {num_2nd} = {int(num_1st/num_2nd)}")
print(f"{num_1st} * {num_2nd} = {int(num_1st*num_2nd)}")